import mongoose from "mongoose";

let userSchema = new mongoose.Schema({
    fullname: {
        type: String,
        required: true,
        trim: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    role:{
        type: String,
        default: "user"
        
    }

}, {timestamps: true})

let User = mongoose.model("User", userSchema, "users");

export default User